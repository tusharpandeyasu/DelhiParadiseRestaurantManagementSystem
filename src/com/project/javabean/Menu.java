package com.project.javabean;

public class Menu {
	
	private int food_id;
	private String food_name;
	private String food_description;
	private String food_image_path;
	private int food_type;
	private double food_price;
	private String cook;
	private String drink_type;
	
	
	
	public String getCook() {
		return cook;
	}
	public void setCook(String cook) {
		this.cook = cook;
	}
	public String getDrink_type() {
		return drink_type;
	}
	public void setDrink_type(String drink_type) {
		this.drink_type = drink_type;
	}
	public double getFood_price() {
		return food_price;
	}
	public void setFood_price(double food_price) {
		this.food_price = food_price;
	}
	public int getFood_id() {
		return food_id;
	}
	public void setFood_id(int food_id) {
		this.food_id = food_id;
	}
	public String getFood_name() {
		return food_name;
	}
	public void setFood_name(String food_name) {
		this.food_name = food_name;
	}
	public String getFood_description() {
		return food_description;
	}
	public void setFood_description(String food_description) {
		this.food_description = food_description;
	}
	public String getFood_image_path() {
		return food_image_path;
	}
	public void setFood_image_path(String food_image_path) {
		this.food_image_path = food_image_path;
	}
	public int getFood_type() {
		return food_type;
	}
	public void setFood_type(int food_type) {
		this.food_type = food_type;
	}
	
	

}
